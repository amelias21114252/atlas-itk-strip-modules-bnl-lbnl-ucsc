def get_status(hx, ml):
    notes = []

    if hx in status_info:
        notes.extend(status_info[hx])

    if ml in status_info:
        notes.extend(status_info[ml])

    if notes:
        return "❌", "<br>".join(notes)

    return "✅", "OK"

