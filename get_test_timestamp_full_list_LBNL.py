def add_sn_prefix(s):

    if s is None:

        return ""

    s = str(s).strip()

    if not s:

        return ""

    if s.startswith("SBML"):

        s = "20U" + s

    return s if s.startswith("SN") else f"SN{s}"

def remove_sn_prefix(s):

    if s is None:

        return ""

    s = str(s).strip()

    if s.startswith("SN"):

        s = s[2:]

    if s.startswith("SBML"):

        s = "20U" + s

    return s

def format_timestamp(ts):

    """

    Convert:

    2025-05-29T18:47:56.395Z

    ->

    2025-05-29 18:47:56

    Removes milliseconds.

    """

    if not ts:

        return ""

    try:

        ts_no_ms = ts.split(".")[0]

        dt = datetime.strptime(

            ts_no_ms,

            "%Y-%m-%dT%H:%M:%S"

        )

        return dt.strftime("%Y-%m-%d %H:%M:%S")

    except Exception:

        return (

            ts.split(".")[0]

            .replace("T", " ")

            .replace("Z", "")

        )

def parse_raw_ml_hx_text(raw_text):

    """

    Returns:

    pairs: list of {"ml": ..., "hx": ...}

    hx_to_ml: dict HX -> ML

    ml_only_pairs: list of ML entries with no HX

    """

    pairs = []

    hx_to_ml = {}

    ml_only_pairs = []

    for line in raw_text.strip().splitlines():

        line = line.strip()

        if not line:

            continue

        parts = [x.strip() for x in line.split(",")]

        ml = parts[0] if len(parts) > 0 else ""

        hx = parts[1] if len(parts) > 1 else ""

        ml = remove_sn_prefix(ml)

        hx = remove_sn_prefix(hx)

        if hx.upper() == "MISSING_HX":

            hx = ""

        pair = {

            "ml": ml,

            "hx": hx,

        }

        pairs.append(pair)

        if hx:

            hx_to_ml[hx] = ml

        else:

            ml_only_pairs.append(pair)

    return pairs, hx_to_ml, ml_only_pairs

